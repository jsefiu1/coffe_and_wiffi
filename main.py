# all Flask routes below
@app.route("/")
def home():
    return render_template("index.html")

@app.route('/add', methods=['GET', 'POST'])
def add_cafe():
    form = CafeForm()
    if form.validate_on_submit():
        # Collect form data
        cafe_name = form.cafe.data
        location = form.location.data
        open_time = form.open.data
        close_time = form.close.data
        coffe = form.coffe.data
        wiffi = form.wiffi.data
        power = form.power.data
        
        # Append to cafe-data.csv
        with open('cafe-data.csv', mode='a', newline='') as file:
            writer = csv.writer(file)
            writer.writerow([cafe_name, location, open_time, close_time, coffe, wiffi, power])
        
        flash('Cafe information added successfully!', 'success')
        return redirect(url_for('add_cafe'))  

    return render_template('add.html', form=form)


@app.route('/cafes')
def cafes():
    with open('cafe-data.csv', newline='', encoding='utf-8') as csv_file:
        csv_data = csv.reader(csv_file, delimiter=',')
        list_of_rows = []
        for row in csv_data:
            list_of_rows.append(row)
    return render_template('cafes.html', cafes=list_of_rows)


